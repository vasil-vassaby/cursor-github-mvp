export function buildPrompt({
    businessNiche,
    product,
    targetAudience,
    textType,
    tone,
    length,
}) {
    const cleanedNiche = businessNiche.trim();
    const cleanedProduct = product.trim();
    const cleanedAudience = targetAudience.trim();

    return [
        "Ты профессиональный копирайтер, который помогает экспертам",
        "в нише здоровья и работы с ресурсом тела (например, ТКМ,",
        "телесные практики, осознанный уход).",
        "",
        `Задача: написать ${textType} для ниши «${cleanedNiche}»`,
        `и продукта «${cleanedProduct}» для аудитории`,
        `«${cleanedAudience}».`,
        "",
        "Требования к стилю:",
        `- тон: ${tone} (естественный, разговорный, без канцелярита);`,
        `- длина: ${length.toLowerCase()} по объёму;`,
        "- без агрессивных обещаний, чудо-результатов и запугивания;",
        "- без категоричных диагнозов и медицинских гарантий;",
        "- поддерживающий, бережный подход к читателю.",
        "",
        "Структура результата:",
        "1) 3 варианта короткого, цепляющего заголовка;",
        "2) основной текст с абзацами, который показывает логику",
        "   подхода, заботу о теле и реалистичные ожидания;",
        "3) 3 мягких варианта призыва к действию (CTA) — приглашение",
        "   на консультацию/разбор/диалог без давления;",
        "4) короткий блок «Важно» с аккуратным дисклеймером,",
        "   что это не замена врачу и диагностике.",
        "",
        "Избегай сложных терминов без пояснений. Пиши понятно,",
        "как для умного, но уставшего человека, который ищет",
        "бережную опору, а не очередное чудо-средство.",
    ].join(" ");
}
